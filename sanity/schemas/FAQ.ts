import { defineField, defineType } from 'sanity';

export const FAQ = defineType({
  name: 'faq',
  title: 'FAQ',
  type: 'document',
  fields: [
    defineField({
      name: 'question',
      title: 'Question',
      type: 'string',
      validation: (rule) => rule.required(),
    }),
    defineField({
      name: 'answer',
      title: 'Answer',
      type: 'string',
      validation: (rule) => rule.required(),
    }),
  ],
});
